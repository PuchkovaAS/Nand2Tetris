// projects/4/Fill.asm
// Бесконечный цикл: опрос клавиатуры → отрисовка

(LOOP)
    // 1. ПРОВЕРКА КЛАВИАТУРЫ
    @KBD
    D=M             // D = код нажатой клавиши (или 0)
    @FILL_SCREEN    // Если D != 0, прыгаем на закраску
    D;JNE           // JNE = Jump if Not Equal (если D != 0)
    
    // 2. РЕЖИМ ОЧИСТКИ (клавиша отпущена)
    @SCREEN      // A = 16384
    D=A
    @ptr
    M=D          // ptr = 16384 (указатель на текущую ячейку экрана)

    @8192
    D=A
    @cnt
    M=D          // cnt = 8192 (счётчик оставшихся слов)

    (CLEAR_LOOP)
    @ptr
    A=M          // A = ptr (указываем на текущую ячейку)
    M=0         // Записываем 0
    D=A+1        // D = следующий адрес
    @ptr
    M=D          // ptr = D (двигаем указатель вперёд)

    @cnt
    M=M-1        // cnt = cnt - 1
    D=M
    @CLEAR_LOOP
    D;JGT        // Если cnt > 0, повторяем цикл
    @LOOP           // Кадр нарисован, проверяем клавиатуру снова
    0;JMP

    // 3. РЕЖИМ ЗАКРАСКИ (клавиша нажата)
(FILL_SCREEN)

    @SCREEN      // A = 16384
    D=A
    @ptr
    M=D          // ptr = 16384 (указатель на текущую ячейку экрана)

    @8192
    D=A
    @cnt
    M=D          // cnt = 8192 (счётчик оставшихся слов)

    (FILL_LOOP)
    @ptr
    A=M          // A = ptr (указываем на текущую ячейку)
    M=-1         // Записываем -1 (все биты = 1 → чёрный цвет)
    D=A+1        // D = следующий адрес
    @ptr
    M=D          // ptr = D (двигаем указатель вперёд)

    @cnt
    M=M-1        // cnt = cnt - 1
    D=M
    @FILL_LOOP
    D;JGT        // Если cnt > 0, повторяем цикл
    
    @LOOP           // Кадр нарисован, проверяем клавиатуру снова
    0;JMP